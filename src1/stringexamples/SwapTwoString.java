package stringexamples;

public class SwapTwoString {

	public static void main(String[] args)
	{
       String s1="sharad";
       String s2="pokale";
       System.out.println("String Before Swap: "+s1+" "+s2);
       String temp=s1;
       s1=s2;
       s2=temp;
       System.out.println("String After Swap: "+s1+" "+s2);
       
	}

}
