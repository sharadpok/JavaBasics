package whileLoop;

public class SimpleExample {

	public static void main(String[] args)
	{
		/*Syntax is as follows:
		while(condition)
		{
			code to be executed(Here initialization and incre/decre part is not in the syntax).
		}*/  
		int i=1;
		while(i<=10)
		{
			System.out.println(i);
			i++;
		}

	}

}
