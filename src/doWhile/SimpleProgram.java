package doWhile;

public class SimpleProgram {

	public static void main(String[] args)
	{
		/*syntax:
			do
			{
			code to be executed; 
			}while(condition) 
       */
		int a=10,b=20;
		do
		{
			System.out.println("Hello");
		}while(a<b);
		/*Basic Rule: If this loop gets true, then it executes for the
		infinite times, but if gets false then it executes only for 
		once and false statement is also get executed.*/
	}

}
