package switchStatements;

public class CharacterSwitchExample {

	public static void main(String[] args)
	{
		char owel='a';
		switch(owel)
		{
		case 'a':System.out.println("It is a character");
		break;
		case 'e':System.out.println("It is e character");
		break;
		case 'i':System.out.println("It is i character");
		break;
		case 'o':System.out.println("It is 0 character");
		break;
		case 'u':System.out.println("It is u character");
		break;
		
		default:System.out.println("It is not owel");
		}

	
	}

}
